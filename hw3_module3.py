"""
Розробіть функцію normalize_phone(phone_number), що нормалізує телефонні номери
до стандартного формату, залишаючи тільки цифри та символ '+' на початку. 
Функція приймає один аргумент - рядок з телефонним номером у будь-якому форматі 
та перетворює його на стандартний формат, залишаючи тільки цифри та символ '+'. 
Якщо номер не містить міжнародного коду, функція автоматично додає код '+38' 
(для України). Це гарантує, що всі номери будуть придатними для відправлення SMS.
"""
import re

def normalize_phone(phone_number: str) -> str:
    """
    Нормалізує телефонні номери до міжнародного стандартного формату для України, 
    залишаючи тільки цифри та символ '+'.
    Аргумент: phone_number: str - рядок з телефонним номером у будь-якому форматі.
    Якщо рядок починається з '+', зберігаємо '+' та беремо лише цифри після нього.
    Якщо цифри починаються з '380' або '38' — додаємо '+' перед ними.
    Якщо цифри починаються з '0' (локальний формат) — додаємо '+38' попереду.
    Інакше — додаємо '+38', як резервний варіант.
    """
    if phone_number is None:
        return ''
    s = phone_number.strip()
    if s.startswith('+'):
        digits = re.sub(r'\D', '', s[1:])
        if not digits:
            return '+'  
        return '+' + digits
    
    digits = re.sub(r'\D', '', s)
    if not digits:
        return ''
    if digits.startswith('380'):
        return '+' + digits
    if digits.startswith('38'):
        return '+' + digits
    if digits.startswith('0'):
        return '+38' + digits
    
    return '+38' + digits


raw_numbers = [
    "067\t123 4567",
    "(095) 234-5678\n",
    "+380 44 123 4567",
    "380501234567",
    "    +38(050)123-32-34",
    "     0503451234",
    "(050)8889900",
    "38050-111-22-22",
    "38050 111 22 11   ",
]

sanitized_numbers = [normalize_phone(num) for num in raw_numbers]
print("Нормалізовані номери телефонів для SMS-розсилки:", sanitized_numbers)
